package com.simple;

public class SwiitchCaseDemo 
{
	public static void main(String[] args) 
	{
		int no=1;
		switch(no)
		{
		case 1:
			System.out.println("Your no is One...");
			break;
		case 2:
			System.out.println("Your no is Two...");
			break;
		case 3:
			System.out.println("Your no is Three...");
			break;
		default:
			System.out.println("Invalid No");
			break;
		}
		
	}

}
